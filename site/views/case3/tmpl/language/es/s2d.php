    <h5>¿Qué es una Unidad de Aprendizaje?</h5>

    <p>Son los componentes de una cualificación de acuerdo con un conjunto existente de conocimiento, habilidades, competencias que han de ser evaluados y validadas.</p>
    <p>Para diseñar unidades de aprendizaje,  previamente se han de identificar un conjunto de resultados de aprendizaje que guardan una conexión específica. Los resultados de aprendizaje pueden formar parte de una especialización concreta (tales como la contabilidad,  la legislación laboral, etc.) o pueden relacionarse con el cumplimiento de una tarea profesional definida (como el aseo personal, la preparación de alimentos, aspectos financieros, etc.).</p>

