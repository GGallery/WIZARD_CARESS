<p>
    El primer paso para definir un módulo de aprendizaje compensatorio para un determinado profesional en un país europeo es la <strong>identificación de una carencia competencial que quiera ser desarrollada.</strong> Para ello se  deben tener en cuenta diferentes perspectivas que deben reflejar la información y los datos existentes sobre un profesional en un país previamente seleccionado.
</p>

<span class="focus">
        <p>RECOGE LA SIGUIENTE INFORMACIÓN</p>
    </span>

<ul class="listapallino">
    <li><strong>Competencias actuales que tiene el profesional y tareas que desarrolla:  </strong></li>
    <ul class="listaspunta">
        <li>¿Qué competencias tienen actualmente los profesionales que desarrollan su actividad en atención  domiciliaria en un país determinado?.
        </li>
        <li>¿Qué tareas desarrolla ese profesional actualmente en los hogares de los adultos mayores?. ¿Qué tareas tiene que desarrollar ese profesional de acuerdo con las necesidades que tienen los adultos mayores?.
        </li>

    </ul>


    <li><strong>Niveles educativos y trayectorias formativas profesionales:</strong></li>
    <ul class="listaspunta">
        <li>¿Qué currículo tiene que tener un profesional que quiera trabajar el sector de atención domiciliaria?.
        </li>
        <li>¿Existen cursos de formación profesional permanente orientados a la atención domiciliaria en adultos mayores para estos profesionales?.
        </li>

    </ul>


    <li><strong>Necesidades relacionadas con el contexto: </strong></li>
    <ul class="listaspunta">
        <li>¿Cuáles son las necesidades de un determinado país de acuerdo a la atención domiciliaria?.
        </li>
        <li>¿Cómo se organiza la atención domiciliaria en un determinado país?. ¿Qué necesidades organizativas se derivan de esto?.
        </li>
        <li>¿Es necesario que estos profesionales trabajen en equipo o en estrecha colaboración con otros profesionales?.
        </li>
        <li>¿Se usan las Tecnologías de la Información y la Comunicación (TIC) para proporcionar servicios de apoyo en atención domiciliaria? ¿Qué tipo de competencias tienen que tener estos profesionales para poder usarlas?.
        </li>
    </ul>

</ul>

<h5>Si necesitas ayuda para obtener esta información puedes buscar en el sistema de CARESS: <a href="https://caress.gsic.uva.es/" target="_blank">https://caress.gsic.uva.es/</a> </h5>

<p>Si alguna información no se encuentra disponible para un país determinado, puedes tratar de buscarla en un país vecino o similar. También te puedes referir a cualquiera de los 9 profesionales en atención domiciliaria que están definidos en el sistema y buscar información en profesiones similares. Debes integrar la información que recojas con la que ya conozcas sobre el tema para contextualizar mejor tu Módulo de Aprendizaje Compensatorio (MAC).</p>

