    <p>
        La definición de los criterios de evaluación a menudo constituyen una de las últimas fases de un proceso de diseño. En realidad, después de definir los resultados de aprendizaje y las estrategias de tu MAC, ya tienes la información necesaria para definir unos criterios de evaluación de manera precisa. 
    </p>

    <span class="focus">
        <p>DEFINE LOS CRITERIOS DE EVALUACIÓN y los REQUISITOS</p>
    </span>

    <p>
        <strong>Los resultados de aprendizaje se definen usando “verbos que impliquen una acción”</strong> (Acción 2-Paso 3). Esto puede ayudarnos a identificar <strong>qué es lo que el estudiante tiene que hacer para mostrar su nivel de adquisición en relación a una competencia</strong>. El resultado de aprendizaje debe también reflejar <strong>el nivel esperado de competencia</strong> así como <strong>el nivel de responsabilidad y autonomía de un aprendiz</strong>.
    </p>

    <p>
        Para definir los CRITERIOS de evaluación esta información debe ser integrada en la <strong>definición de REQUISITOS DE EVALUACIÓN.</strong>
    </p>

    <p>
        Algunas veces, <strong>una descripción de una situación profesional </strong>puede ser muy útil, así como la referencia a actividades clave analizadas al principio del proceso de diseño (Actividad 1-Paso 2). Otros <strong>requerimientos adicionales</strong>, han de ser definidos; por ejemplo para simular entornos y condiciones reales de trabajo, puede ser útil asignar un tiempo determinado al desempeño de cada actividad de aprendizaje. De esta manera, se pretende que la persona pueda desarrollar una actividad concreta simulando las condiciones más reales posibles en las que tendrá que poner en marcha esa competencia o aprendizaje, de acuerdo con unas condiciones de estrés determinado; el equipamiento disponible ha de ser claramente definido, así como aquellos materiales, productos o soportes tecnológicos concretos para la situación profesional determinada que quiera ser evaluada. 
    </p>
    <p>
        De esta manera, el diseño de evaluación debe de profundizar en la <strong>definición de herramientas específicas </strong>como por ejemplo test) <strong>o actividades</strong> (de resolución de problemas, demostraciones, etc.).

    </p>

    <span class="focus">
        <p>DEFINE HERRAMIENTAS DE EVALUACIÓN Y ACTIVIDADES</p>
    </span>

    <p> La organización de una situación compleja de aprendizaje situado como la elaboración de prácticas o de situaciones cercanas al aprendizaje en contextos laborales,  puede permitir evaluar distintas habilidades y competencias  transversales del aprendiz en contextos reales. Un aspecto  importante  para el  diseño es que el docente tiene  <strong>ser consciente de las competencias  (y sus componentes) que puedan ser evaluables durante esas experiencias prácticas o de aprendizaje en entornos laborales,</strong>  así como organizar de manera efectiva las condiciones para su evaluación. Para ello deberá identificar y definir una serie criterios.</p>
    <p>En este paso, un aspecto importante sería integrar la información sobre evaluación en las herramientas de diseño que hemos utilizado hasta ahora (tablas de diseño)..</p>

