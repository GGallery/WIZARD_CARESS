<h5>¿Qué son los conocimientos, habilidades y competencias transversales y personales? </h5>

<p>
    <strong>CONOCIMIENTO:</strong> es el resultado de la asimilación de información a través de un aprendizaje. El conocimiento es el corpus de hechos, teorías y prácticas relacionadas con un campo/área de trabajo o de estudio.
</p>
<p>
    <strong>HABILIDAD:</strong> es la destreza de aplicar conocimiento y saber cómo usarlo para completar una determinada tarea y resolver problemas.
</p>

<p><strong>COMPETENCIAS PERSONALES: </strong>engloban aspectos personales, sociales y/o habilidades metodológicas que deben ser desarrolladas en la sociedad y en el trabajo.</p>

<p><strong>COMPETENCIAS TRANSVERSALES: </strong>son aquellas típicamente consideradas aspectos no específicos relacionados con un trabajo o tarea particular dentro de una disciplina académica o área, pero que son usadas en una amplia variedad de situaciones personales y entornos laborales. </p>
<p>En la definición de un "resultado de aprendizaje" las <strong>COMPETENCIAS TRANSVERSALES Y PERSONALES </strong>son descritas en términos de "competencias necesarias para aplicar un conocimiento y/o habilidad en un <strong>contexto laboral con cierto nivel de responsabilidad y autonomía".</strong></p>

