<h5>¿Qué es una competencia?</h5>

<p>De manera convencional en el proyecto CARESS el término competencia se ha utilizado para describir la capacidad de aplicar o usar un conocimiento y habilidad determinada (con un determinado nivel de responsabilidad y autonomía) en un entorno de trabajo definido para desarrollar funciones laborales críticas o desarrollar tareas con éxito; esta característica incluye tanto competencias visibles en forma de conocimiento y habilidades así como elementos subyacentes tales como aspectos actitudinales.</p>

<span class="focus">
        <p>PIENSA SOBRE "ACTIVIDADES CLAVE"  que deban ser desarrolladas por un profesional determinado y HAZ UNA LISTA</p>
