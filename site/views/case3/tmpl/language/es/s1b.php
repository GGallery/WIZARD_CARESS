<h5>¿Qué son actividades clave? </h5>
<p>
    <strong>Las actividades clave constituyen un grupo de competencias</strong> que son necesarias para desarrollar una tarea relevante de acuerdo a un perfil profesional. Las actividades clave de una profesión deben, de manera conjunta, cubrir todas las actividades para el desarrollo de una profesión, de acuerdo a su aplicación en un contexto determinado. 
    <br>
    <em>EAlgunos ejemplos de actividades clave pueden ser "formar a los usuarios y a sus cuidadores" o “monitorizar la fragilidad y las condiciones de salud”. Las actividades clave podrían ser agrupadas también en ROLES profesionales (véase Glosario de CARESS), tales como "prevención y educación en salud" o "investigación". </em>
</p>

<span class="focus">
        <p>DEFINE las COMPETENCIAS que deben caracterizar cada "actividad clave" y HAZ UNA LISTA </p>
    </span>

<p>Las competencias pueden ser definidas como el "Conocimiento" y las "Habilidades" necesarias para desarrollar unas actividades clave con el nivel de responsabilidad y autonomía que se espera que el profesional adopte en el desarrollo de una actividad clave. De esta manera, puedes distinguir entre: 
</p> 

<ul class="listapallino">
    <li>
        <em>Competencias que son horizontales a diferentes profesionales implicados en atención domiciliaria (carencia competencial transversal), tales como el conocimiento de las reglas y la legislación de acuerdo a la atención domiciliaria, o las habilidades prácticas que son necesarias para usar TIC en la monitorización de la salud, etc. 
    </li>
    <li>
        <em>Competencias que son específicas a un determinado profesional, tales como  habilidades prácticas en relación con ejercicios terapéuticos o de rehabilitación orientados a adultos mayores desarrollados por los fisioterapeutas, o el conocimiento de una red local de servicios sociales para asistentes a domicilio.  
    </li>
</ul>

