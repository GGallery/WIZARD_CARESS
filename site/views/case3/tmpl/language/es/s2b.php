<h5>¿Qué es un resultado de aprendizaje?</h5>

<p>Existen declaraciones o evidencias de lo que un aprendiz sabe, entiende y es capaz de realizar al finalizar un proceso de aprendizaje; estos aspectos se pueden definir en términos de conocimiento, habilidades y competencias</p>

<h5>¿Cuál es la diferencia entre un resultado de aprendizaje y las competencias? </h5>

<p>Las competencias en realidad son resultados de aprendizaje que han sido alcanzados y validados por la demostrada capacidad para utilizar conocimientos de manera autónoma y desarrollar habilidades personales, sociales y metodológicas en situaciones de trabajo o estudio y en el desarrollo profesional y personal. </p>

