<h5>¿Qué es un Módulo de Aprendizaje Compensatorio?</h5>

<p>
    Es un módulo de aprendizaje que aborda una determinada "carencia formativa" en forma de competencia que puede tener un profesional en atención domiciliaria en un país europeo previamente elegido. El término compensatorio se refiere a que la información existente se ha obtenido de una revisión del currículo formativo de estos profesionales así como de cursos de formación profesional existentes para estos colectivos. Esa revisión nos ha permitido identificar una serie de carencias formativas que presentan distintos profesionales en atención domiciliaria y que se encuentran expresados en forma de competencias. 
    Cada MAC está compuesto por un número de Unidades de Aprendizaje (definidas en conocimientos, habilidades y competencias de acuerdo con ECVET). Para saber más puedes consultar nuestro glosario.
</p>
