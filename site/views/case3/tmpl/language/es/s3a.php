<p>
    Después de identificar un listado de resultados de aprendizaje y agruparlos en unidades de aprendizaje, el siguiente paso que tendrás que completar tiene que ver con la definición de estrategias específicas de aprendizaje que usarás para logar la consecución de cada resultado.
</p>
<p>
    El proceso de diseño tendrá que tener en cuenta  aquellas  <strong>“limitaciones del diseño”, que caracterizan tanto el curso inicial como el módulo de aprendizaje compensatorio (MAC).</strong>
</p>
Ten en cuenta que ya ha analizado algunas de las limitaciones contextuales (Acción 1-Paso 1), pero necesitas asegurarte que todas las dimensiones han sido tenidas en cuenta.
</p>


<span class="focus">
        <p>BUSCA UNA RESPUESTA A ESTAS PREGUNTAS</p>
    </span>

<ul class="listaspunta">
    <li>¿Cuáles son las características principales del colectivo en el que te vas a centrar (los aprendices)?. ¿La mayor parte de ellos se encuentran trabajando actualmente? ¿Son recién graduados? ¿Cuánto tiempo pueden dedicar a un curso de formación profesional? ¿Puede ser útil para ellos tener algunas experiencias prácticas cercanas al ámbito laboral?. ¿Podría ser útil para ellos que parte del curso se desarrollara en un ámbito de aprendizaje en línea?. ¿Tienen un nivel de competencia TIC suficiente?</li>
    <li>¿Existe alguna ley o norma (nacional o local) que regule la formación profesional en atención domiciliaria que pueda afectar a la organización de tu curso?. Piensa en el número de horas que tendrá el curso, los prerrequisitos  para el reconocimiento de la cualificación, etc. ¿Puede el aprendizaje informal reconocerse de alguna manera?</li>
    <li>¿Cuáles son los recursos de los que dispones (<em>económicos, logísticos, técnicos, humanos, etc.</em>)?. ¿Puedes describirlos?. ¿Puedes elaborar nuevos materiales? ¿Tienes recursos de aprendizaje previos o materiales que puedas reutilizar en este curso? ¿Puedes pagar a expertos. ¿Tienes los recursos para garantizar la implicación de profesores especializados? ¿Cuentas con espacios y equipamiento adecuado para las clases presenciales?. ¿Tienes alguna plataforma de aprendizaje en línea?</li>
</ul>

<p>En general, debes tener en cuenta los siguientes aspectos:</p>

<ul>
    <li>La práctica es un aspecto esencial en la atención domiciliaria de adultos mayores y las políticas europeas fomentan cada vez más que los profesionales adquieran aprendizajes conectados al ámbito laboral. Sin embargo, el aprendizaje en entornos laborales puede ser complicado de gestionar cuando los “entornos de trabajo” son los hogares de los adultos mayores; en muchos países  los aprendizajes a menudo se limitan a contextos institucionales o a residencias de adultos mayores, debido a problemas organizativos y de seguridad. Sin embargo, en otros países (especialmente en el norte de Europa) las prácticas están bien integradas en los sistemas de formación profesional. </li>
    <li>El conocimiento debe ser  <strong>situado, contextualizado y unido a la práctica</strong>, y esto es especialmente cierto en el campo de la salud y la asistencia social: los esquemas mentales y los procedimientos son aspectos tan fundamentales, como las competencias transversales personales y sociales.  Por tanto, el desarrollo de competencias no formales e informales en los procesos de aprendizaje es altamente recomendado.</li>
    <li>Cada país o región generalmente tiene su <strong>propio reglamento de acuerdo a cómo se organiza el sistema de formación profesional</strong>; estos aspectos se han de tener en cuenta para proponer un curso que esté bien alineado con los sistemas de reconocimiento propuestos por los agentes reguladores en un país determinado.</li>
</ul>

