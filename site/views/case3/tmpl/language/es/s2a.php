<p>
    Uno de los primeros pasos en el diseño de un proceso de aprendizaje es la definición de las limitaciones contextuales.
    Como su propósito es diseñar un Módulo de Aprendizaje Compensatorio, basado en una iniciativa de formación profesional ya existente en lugar de crear un curso completamente nuevo e intentar mejorarlo, el primer paso es:
</p>

<span class="focus">
        <p>IDENTIFICAR EL COLECTIVO AL QUE SE DIRIGE (los estudiantes)</p>
    </span>

<p>
   Aunque en este paso el colectivo profesional al que te diriges en un país determinado ya ha sido definido, se puede <strong>añadir o completar información para que esté mejor definido.</strong>.
</p>

<span class="exampleBox">
Por ejemplo, si has elegido “ENFERMEROS/AS” en ITALIA”, necesitas ser más específico:
    <em>"Qué tipo de enfermeros/as"? ¿graduados/as en los últimos 5 años?
¿Tienen una especialización específica?"</em>
La profesión de enfermería en Italia es una profesión reconocida con un Grado académico desde el año 1999, pero muchos profesionales que trabajan como enfermeros/as en atención domiciliaria no lo tienen. 
    <strong>¿Cuáles son las características específicas de tu colectivo? </strong>

</span>

<p>
   Después de definir las características precisas de tu colectivo debes
</p>

<ul class="listapallino">
    <li>Escoger un curso de formación profesional existente en atención domiciliaria (preferiblemente, si estuviera disponible).</li>
    <li>Escoger un curso de formación profesional, no centrado específicamente en atención domiciliaria pero en el que se trabajen algunas habilidades que quieras integrar en tu diseño.</li>
    <li>Escoger un curso de formación inicial, como la información procedente del plan de estudios de un Grado en Enfermería, y crear un curso de especialización a partir del mismo.</li>

    </li>
</ul>

Desde ahora llamaremos a este curso tu <strong>“curso inicial”</strong>
