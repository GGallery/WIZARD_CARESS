<h5>Mitkä oppimistulokset ovat?</h5>

<p>Oppimistulokset kuvaavat, mitä opiskelija on saavuttanut oppimistyönsä seurauksena, ja mitä hän tietää, ymmärtää ja pystyy tekemään oppimisprosessin päätteeksi Oppimistulokset, jotka määritellään tietoina, taitoina ja pätevyyksinä.</p>

<h5>Miten oppimistulokset ja osaaminen (pätevyys) erotetaan?</h5>

<p>Osaaminen on oppimistulos, joka on saavutettu tietojen ja taitojen kehittymisen kautta näin, että tietoja ja taitoja voidaan soveltaa tai käyttää uudessa tilanteessa tai työympäristössä vastuullisesti ja itsenäisesti.</p>

<p>Ennen kun voidaan kuvata uuden täydennyskurssin oppimistulokset, pitää tarkistaa olemassa olevan tutkinnon tai kurssin oppimistulokset ja niihin verrattuna kotihoidon osaamisvaje.</p>

