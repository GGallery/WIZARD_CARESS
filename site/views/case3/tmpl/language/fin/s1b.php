<h5>Miten pätevyys (osaaminen) määritellään?</h5>

<p>Pätevyys (osaaminen) kuvataan Eurooppalaisen tutkintojen viitekehyksen (EQF) mukaan vastuun ja itsenäisyyden perusteella. CARESS-hankkeessa osaamista käytetään kuvaamaan kykyä soveltaa tai käyttää tiettyä tietoja ja taitoja (sisältäen vastuun ja itsenäisyyden), jota tarvitaan määritellyssä työympäristössä tehtävien suorittamiseen menestyksekkäästi.</p>

<span class="focus">
        <p>KESKITTYKÄÄ kotihoidon ammatinharjoittajan keskeisiin TYÖTEHTÄVIIN ja LISTATKAA NE </p>
    </span>

<h5>Mitkä ovat keskeiset työtehtävät?</h5>
<p>
    <strong>Keskeiset työtehtävät ovat työn profiilin</strong>, merkityksellisen tehtävän suorittamisessa integroitu osaamisryhmä, jotka ovat kaiken kaikkiaan tarpeellisia työn profiilin kannalta. Työtehtävät vaativat ammatinharjoittajalta monenlaista osaamista.
Esimerkkejä keskeisistä työtehtävistä voisi olla ”arvioi asiakkaan hoidon ja kuntoutumisen tarvetta ja sen
kiireellisyyttä" tai " huomioi asiakkaan yksilöllisen elämäntilanteen” (ks. CARESS sanasto).</em>
</p>

<span class="focus">
        <p>TUNNISTAKAA keskeisiä työtehtäviä kuvaavat OSAAMISVAATIMUKSET JA LISTATKAA NE</p>
    </span>

<p>Osaamisvaatimukset voidaan määritellä edellä mainittujen työtehtävien suorittamiseksi tarvittavan tietämyksen ja taitojen sekä vastuullisuuden ja itsenäisyyden perusteella.
Lisäksi voitte erottaa:

</p>

<ul class="listapallino">
    <li>
        <em>Horisontaaliset osaamisvaatimukset, osaaminen, mitä kotihoidon eri ammattien edustajat tarvitsevat</em>, kuten sääntöjen ja lakien tunteminen, IT-taidot jne.
    </li>
    <li>
        <em>•   Ammattiin liittyvä erityisosaaminen</em>, kuten fysioterapeutti osaa neuvoa asiakkaalle hänen yksilöllisiin tarpeisiin sopia käytännön harjoituksia, lähihoitaja tuntee sosiaalipalvelujen tarjoamia mahdollisuuksia  jne.
    </li>
</ul>

