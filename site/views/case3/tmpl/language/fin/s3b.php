<p>
    Ottakaa esille uuden täydentävän tutkinnonosan oppimistulosten taulukko.
</p>

<span class="focus">
        <p>LÖYTÄKÄÄ OPPIMISTULOKSILLE SOPIVAT OPETUSMENETELMÄT</p>
    </span>

<p>
    Suunnitelkaa opetusmenetelmät ottaen huomioon tavoitteena olevien <strong>oppimistulosten sisältö, tehtävien konteksti ja mahdollinen kohderyhmä sekä opiskelijoiden eri oppimisstrategiat.</strong>
</p>

<p>Lisätkää valitsemanne opetusmenetelmät aiempaan taulukkoon (Taso 2 – askel 3).</p>

<a href="components/com_wizard/files/annex5_fin.docx" class="templateLink" target="_blank">Paina ikonia ja lataa opetusmenetelmien taulukko</a>

<p>
    Kukin oppimistulos on saavutettavissa monen eri opetusmenetelmän avulla. Samoin, samalla menetelmällä on mahdollista saavuttaa monta eri oppimistulosta. Erilaisissa tehtävissä ja aiheissa tarvitaan erilaisia oppimisstrategioita. Tärkeää on tarjota eri menetelmiä ottaen huomioon opiskelijoiden henkilökohtaiset erot ja tarpeet oppijana, heidän eri oppimisstrategiat. Asenteilla ja tunteilla on suuri merkitys oppimiseen.
</p>

<span class="focus">
        <p>OPETUSMENETELMIEN PITÄÄ TUKEA JA MOTIVOIDA OPISKELIJAA OPPIMISTAVOITTEDEN SAAVUTTAMISESSA</p>
    </span>

<p>
    Tutustukaa oppimisstrategiateorioihin ja oppimisympäristöihin.
</p>


<a href="components/com_wizard/files/annex6_fin.pdf" class="templateLink" target="_blank">Paina ikonia ja lataa lyhyt luettelo vihjeistä, joita voit ottaa huomioon määritelläksesi oikean oppimisstrategian</a>

<p>
    Jos maankohtaiset ja alakohtaiset ehdot sallivat, suunnitelkaa yksi tai useampi mooduli verkkokurssina. Erityisesti aikuiskoulutuksessa on suositeltavaa käyttää verkko-oppimismahdollisuuksia.
</p>


