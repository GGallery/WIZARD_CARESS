<p>
   Tämä suunnittelutyökalu tarjoaa ideoita ja ohjeita, miten CARESS-hankkeen materiaalin avulla suunnitella uusia opintosisältöjä ja täydennyskoulutuskursseja.
</p>

<h5>Mikä on täydentävä tutkinnon osa?</h5>

<p>
    Se on EU-maissa sosiaali- ja terveysalan tutkintojen kotihoidon osaamisvajeeseen vastaava tutkinnon osa. CARESS-hankkeessa käytetään siitä nimikettä "korvaava", koska se perustuu jo olemassa oleviin opetussuunnitelmiin ja ammatillisen koulutuksen kursseihin, ja tarjoaa täydentävää kotihoidon osaamistarpeita vastaavaa koulutusta. Täydentävä tutkinnon osa kuvataan osaamisperusteisena. Lue lisää sanastosta.
</p>
<p>
    Tutkinnon osan suunnittelun suuntaviivoja!
</p>
<p>
    Suunnittelun aloitukseksi valitaan kohdemaan koulutustarpeeseen sopiva lähtötaso ja painetaan Enteriä.
</p>

