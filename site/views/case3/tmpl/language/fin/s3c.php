    <p>
        Arviointikriteereiden määrittely jätetään usein suunnitteluprosessin viimeisille vaiheille. Oikeastaan uuden tutkinnon osan oppimistulosten ja -strategioiden määrittämisen jälkeen on jo olemassa tärkeimmät tiedot, joiden avulla määritellään arviointikriteerit. 
    </p>

    <span class="focus">
        <p>MÄÄRITÄ ARVIOINTIKRITEERIT JA EHDOT</p>
    </span>

    <p>
        <strong>Määritimme oppimistulokset käyttämällä "aktiivisia verbejä"</strong> (Taso 2 - askel 3), mikä auttaa meitä tunnistamaan, mitä <strong>oppimistuloksia opiskelijan pitäisi näyttää osoittaakseen, että hän todellakin hallitsee sisältöä</strong>. Oppimistuloksista pitää myös näkyä, että opiskelija <strong>pystyy ottamaan vastuun tehtävistään ja toimimaan työympäristössä itsenäisesti</strong>.
    </p>

    <p>
        Yhdistäkää arviointikriteerit oppimistuloksiin.  <strong>Arviointi määritellään aina maakohtaisesti</strong> vastaamaan lainsäädäntöä.
    </p>

    <p>
        Joskus kuvaus ammattitilanteesta voi olla erittäin hyödyllinen, samoin kuin viittaus suunnitteluprosessin alussa analysoituihin avaintoimintoihin (taso 1 - askel 2). Lisäksi voidaan tarkemmin määritellä arviointivaatimukset: <strong>arviointipaikka, arviointimenetelmä, kuka saa arvioida jne</strong>. (esimerkiksi osaaminen osoitetaan aina näytöissä, näytöt järjestetään ensisijaisesti työpaikoilla aidoissa työelämän tilanteissa jne).
    </p>
    <p>
 Arvioinnin suunnittelussa olisi syytä määritellä siihen erityisiä työkaluja (esim. arviointilomake, verkkotesti jne) tai toimintoja (esim. opiskelija palauttaa verkossa vaaditut tehtävät, osallistuu vertaisarviointiin, laatii kirjallisen näyttösuunnitelman jne).
    </p>

    <span class="focus">
        <p>MÄÄRITELLÄÄN ARVIOINTITYÖKALUT JA TOIMINNAT</p>
    </span>

    <p>Ammatillisen koulutuksen järjestäminen ja arvioiminen on säädelty maankohtaisesti. Suomessa arviointikriteerit ovat kansallisia (Opetushallitus). Opiskelijalla on oikeus tietää oppimistulokset ja niiden arviointikriteerit jo ennen opiskelunsa aloittamista. Osaamista voi hankkia suorittamalla koko tutkinnon tai aiempaa joustavammin myös tutkinnon osia. Koulutuksen ja näyttöjen järjestäminen tapahtuu opiskelijan henkilökohtaisen osaamisen kehittämissuunnitelman (HOKS) mukaan.</p>
    <p>
Yhdistää arviointiin tarvittavat tiedot aiempaan suunnittelutaulukkoon.
</p>

