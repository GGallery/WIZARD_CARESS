<h5>Miten kuvataan oppimistuloksia?</h5>

<p>Oppimistulosten on oltava ymmärrettäviä, todennettavissa ja arvioitavia, jotta opiskelijat ja opettajat voivat arvioida, onko tuloksia todella saavutettu.</p>

