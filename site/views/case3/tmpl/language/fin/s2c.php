<h5>Miten tiedot, taidot ja osaaminen määritellään?</h5>

<p>
    <strong>TIETO </strong>tarkoittaa oppimalla tapahtuneen asioiden omaksumisen tulosta. Tiedot muodostuvat työ- tai opintoalaan liittyvien faktojen, periaatteiden, teorioiden ja käytäntöjen kokonaisuudesta. Tiedot voivat olla teoria- tai faktatietoja tai molempia.
</p>
<p>
    <strong>TAITO </strong>tarkoittaa kykyä soveltaa tietoja ja käyttää tietotaitoa tehtävien suorittamiseen ja ongelmien ratkaisuun. Taidot kuvataan kognitiivisina (loogisen, intuitiivisen ja luovan ajattelun käyttö) tai käytäntöön liittyvinä (kätevyys ja menetelmien, materiaalien ja työkalujen ja -välineiden käyttö).
</p>

<p><strong>Personal CompetencesOsaaminen (henkilökohtainen ammatillinen erityisosaaminen) </strong>ctarkoittaa todistettua kykyä käyttää tietoja, taitoja sekä henkilökohtaisia, sosiaalisia ja/tai menetelmällisiä valmiuksia työ- tai opintotilanteissa ja ammatilliseen ja henkilökohtaiseen kehitykseen Eurooppalaisessa tutkintojen viitekehyksessä pätevyyttä kuvataan vastuun ja itsenäisyyden perusteella.</p>

<p>Horisontaalinen osaaminen tarkoittaa sellaista pätevyyttä, mikä ei liity tiettyyn ammattiin, tehtävään tai akateemiseen osaamiseen tai osaamisalaan, mutta jota voidaan käyttää monissa tilanteissa ja työympäristöissä.</p>
<p>Oppimistuloksissa HORISONTAALINEN JA AMMATILLINEN PÄTEVYYS kuvataan osaamisena, joka tarvitaan tietojen ja/tai taidon soveltamiseen <strong>uudessa tilanteessa tai työympäristössä vastuullisesti ja itsenäisesti </strong>.</p>

<p>Oppimistuloksien kuvaamisella voitte käyttää seuraavaa lomaketta (EU ENhANCE projekti https://www.enhance-fcn.eu/)</p>
