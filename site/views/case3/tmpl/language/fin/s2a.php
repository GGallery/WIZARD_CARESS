<p>
Uuden kurssin tai tutkinnon osan suunnittelun ensimmäinen askel on ammattiin liittyvien ehtojen tunnistaminen (contextual constraints).
Kun tavoitteena on suunnitella täydentävä tutkinnon osa ja integroida se jo olemassa olevaan tutkintoon, ensimmäisenä pitää: 

</p>

<span class="focus">
        <p>TUNNISTAA TEIDÄN KOHDEMAAN KOHDERYHMÄ (koulutettavat)</p>
    </span>

<p>
    Huolimatta siitä, että kohdemaa ja kohderyhmä on tunnistettu, kohderyhmä tarkentuu aina  <strong>osallistujien henkilökohtaisten tarpeiden</strong> (henkilökohtainen opintosuunnitelma) mukaan.
</p>

<span class="exampleBox">
Esimerkiksi, jos valitsette “NURSES in ITALY” (sairaanhoitaja Italiassa), pitää tarkentaa:
    <em>"mikä on osaamisala/tarkempi koulutusohjelma”? milloin on valmistunut? erikoisosaaminen?"</em>
Sairaanhoitajakoulutusta on järjestetty korkeakoulussa Italiassa vuodesta 1999. Monet Italian kotihoidossa toimivat hoitajat eivät kuitenkaan ole suorittaneet virallista tutkintoa.
    <strong>Ketkä ovat meidän koulutuksen kohderyhmän tarkemmat käyttäjät?</strong>

</span>

<p>
    Tarkemman kohderyhmän tunnistamisen jälkeen:
</p>

<ul class="listapallino">
    <li>valitkaa ensisijaisesti olemassa oleva ammatillinen kotihoidon kurssi (jos on olemassa);</li>
    <li>valitkaa olemassa oleva ammatillinen kurssi, mikä ei liity kotihoitoon, mutta jossa on taitoja, mitä haluatte päivittää;</li>
    <li>valitkaa yleinen valmisteleva kurssi (esimerkiksi hoitoalan tutkinto), minkä pohjalle voisi rakentaa täydentävän erikoiskurssin.</li>

    </li>
</ul>

Jatkossa uudesta kurssista käytetään termiä <strong>“peruskurssi”</strong>
