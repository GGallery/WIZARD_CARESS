<p>
    Uuden erityisen ammatinharjoittajan määrittely kohdemaassa edellyttää, että tunnistetaan hänen mahdolliset työtehtävänsä ja selvitetään osaamisvaatimukset. Selvitetään taustatiedot kohdemaasta, hoitokulttuurista, tarjottavasta koulutuksesta ja tunnistetaan kotihoidon ammatinharjoittajan puuttuva osaaminen. 
</p>

<span class="focus">
        <p>KERÄTKÄÄ SEURAAVIA TAUSTATIETOJA</p>
    </span>

<ul class="listapallino">
    <li><strong>•   Ammattilaisen osaamisvaatimukset ja työtehtävät: </strong></li>
    <ul class="listaspunta">
        <li>Mikä on kohdemaan kotihoidon ammatinharjoittajan todellinen pätevyys ja osaaminen?
        </li>
        <li>Millaisia tehtäviä ammatinharjoittajan on tehtävä asiakkaan kotona? Millaista apua kotihoidon asiakkaat tarvitsevat?
        </li>

    </ul>


    <li><strong>Kotihoidon ammatinharjoittajan koulutustaso ja opetussuunnitelma: </strong></li>
    <ul class="listaspunta">
        <li>Millaista koulutusta vaaditaan kohdemaassa työskennelläkseen kotihoidossa/sosiaal- ja terveysalalla?  
        </li>
        <li>Tarjotaanko sosiaali- ja terveysalan tutkinnoissa kotihoidon koulutusta, kuuluuko tutkintoon ja opetussuunnitelmaan kotihoito? 
        </li>

    </ul>


    <li><strong>Alakohtaiset tarpeet: </strong></li>
    <ul class="listaspunta">
        <li>Mitkä ovat kodinhoidon tärkeimmät kehittämistarpeet kohdemaassa? 
        </li>
        <li>Miten kotihoito järjestetään kohdemaassa? Mitkä ovat siitä johtuvat kehittämistarpeet?
        </li>
        <li>Miten kotihoidon työntekijät toimivat, yksin, tiimeissä ja/tai tiiviissä yhteistyössä muiden asiantuntijoiden kanssa?
        </li>
        <li>Käytetäänkö kotihoidossa hyvinvointiteknologiaa? Millaista IT- ja digiosaamista kotihoidon työntekijä tarvitsee?
        </li>
    </ul>

</ul>

