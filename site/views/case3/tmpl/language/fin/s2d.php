    <h5>Mitä tutkinnon osa on?</h5>

    <p>Tutkinnon osa kuuluu laajempaan tutkintoon, ja on kuvattu tietoina, taitoina ja osaamisena, mitä on mahdollista arvioida ja tunnustaa.</p>
    <p>Tutkinnon osaan kootaan samaan aiheeseen liittyvät oppimistulokset. Nämä oppimistulokset voivat olla osa erityistä erikoistumista (esimerkiksi ergonomia, hyvinvointiteknologia, työlainsäädäntö jne.) tai liittyvät määritellyn ammatillisen tehtävän (kuten lääkehoito jne.) toteuttamiseen.</p>

