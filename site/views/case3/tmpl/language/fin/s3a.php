<p>
    Oppimistulosten tunnistamisen ja ryhmittämisen jälkeen määritellään oppimisstrategiat, toiminnat ja arviointi. Ottakaa huomioon maankohtaiset ja alakohtaiset ehdot ja mahdolliset rajoitteet.
</p>
<p>
   Taustaselvityksen aikana (Toiminta 1 – askel 1) kohdemaan ja kohderyhmän osaamisvaje ja osaamistarve on kuvattu. Analysoikaa uuden tutkinnon osan suunnittelua monesta näkökulmasta seuraavien lisäkysymyksien avulla: 
</p>


<span class="focus">
        <p>LÖYTÄKÄÄ VASTAUKSET SEURAAVIIN KYSYMYKSIIN</p>
    </span>

<ul class="listaspunta">
    <li>Mitkä ovat kohderyhmän (opiskelijat) tausta ja keskeiset tarpeet? Ovatko he vastavalmistuneita vai jo työelämässä? Aikuisia vai nuoria? Ovatko he työelämässä vai työttömiä? Paljonko heillä on opiskeluun aikaa (päivittäin/viikoittain/kuukausittain)? Tarvitsevatko he enemmän käytännön harjoittelua (vai teoriatietoa)? Sopiiko heille verkkokurssi? Onko heillä verkkokurssia varten tarpeelliset IT-taidot ja/tai digiosaaminen?</li>
    <li>Mitä kohdemaan lajinsäädäntö säätää ammatillisesta (kotihoidon) koulutuksesta? Onko olemassa kansallisia tai alueellisia kotihoidon ammatillisen koulutuksen lakia tai asetuksia, jotka voivat vaikuttaa kurssin järjestämiseen? Mitä tarvitaan koulutuksen järjestämiseen (resurssit kuten osaava henkilökunta, välineet, aika, raha jne.) ja millä edellytyksellä kurssilla hankittu osaaminen tunnistetaan ja tunnustetaan (esim. kurssin laajuus)? Miten informaali oppiminen tunnistetaan?</li>
    <li>Mitkä ovat odotettavat (tiedossa olevat) resurssit (taloudelliset, logistiset, tekniset, henkilöresurssit jne.)?  Onko sijoittaja (ym. rahoituskeino) tiedossa? Löytyykö aiempaa opetusmateriaalia ja muita resursseja, joita voidaan käyttää? Voitteko tuottaa uusia materiaaleja? Voitteko maksaa asiantuntijoille? Onko resursseja, joilla taataan oikeiden opettajien osallistuminen? Löytyykö sopiva opetusympäristö ja laitteita, jotka soveltuvat lähiopetukseen? Onko olemassa verkko-oppimisympäristö? </li>
</ul>

<p>Yleistasolla pitää ottaa huomioon:</p>

<ul>
    <li>Koulutuksen on oltava käytännön läheistä ja harjoittelua järjestetään työpaikalla. Työpaikalla järjestettävän koulutuksen muotoja ovat oppisopimuskoulutus ja koulutussopimus. Koulutus työpaikalla suunnitellaan osana henkilökohtaista osaamisen kehittämissuunnitelmaa (HOKS), joka laaditaan kaikille opiskelijoille. Kotihoidossa työpaikkana on asiakkaan koti. Harjoittelu voidaan suorittaa kotihoidon työntekijän työparina, jossa työntekijä on ohjaaja ja/tai arvioija. </li>
    <li>Teoriatiedon pitää linkittyä työympäristöön, tehtäviin ja käytäntöön. Koulutusta pitää suunnitella ottamalla huomioon maankohtaiset, kulttuuriset ja henkilökohtaiset erot. Koulutusta tarjotaan joustavasti ja monimuotoisesti (lähiopetus, verkko-opetus, formaali, informaali ja nonformaali). </li>
</ul>

