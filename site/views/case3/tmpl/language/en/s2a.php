<p>
    One of the first steps in the design of a learning process is the definition of contextual constraints.
    Since your purpose is to design a Compensative Learning Module, based on an already existing VET initiative rather than by creating a whole new course and trying to improve it,  the first step is to:
</p>

<span class="focus">
        IDENTIFY YOUR SPECIFIC TARGET (the learners)
    </span>

<p>
    Although at this step the professional you want to target and his/her country should have already been defined, <strong>target users still need to be better defined</strong>.
</p>

<span class="exampleBox">
For instance, if you choose “NURSES in ITALY” you need to be more specific :
    <em>“which kind of nurses”? graduated in the last 5 years? with a specific specialization? “</em>
NURSES curricula in Italy require an academic degree since 1999, but many nurses working in homecare are not.
    <strong>What are our specific target-users?</strong>

</span>

<p>
    After defining the specific target, you can
</p>

<ul class="listapallino">
    <li>choose an existing VET course about homecare (preferable if available)</li>
    <li>choose an existing VET course, not specifically focused on homecare but targeting some skills that you want to address;</li>
    <li>choose a general propaedeutic course, such as a degree in nursing, on which you can build a specialization course.</li>

    </li>
</ul>

From now on we’ll call this course your <strong>“baseline course”</strong>
