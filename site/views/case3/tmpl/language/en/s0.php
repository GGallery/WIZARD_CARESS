<h5>What is a Compensative Learning Module?</h5>

<p>
    It is a learning module which targets a specific “competency gap” and refers to a specific homecare professional in a EU
    country. We call it “compensative” since it is built upon already existing curricola and VET courses, integrating them whenever  a “competency gap” is identified.
    Each CLM is composed by a number of Learning Units (defined in term of knowledge, skills and competences – according to ECVET). See our Glossary  to know more.
</p>
