    <h5>What is a Compensative Learning Module (CLM)?</h5>

    <p>
        A CLM is a learning module which targets a specific “competency gap”. It refers to a specific homecare professional in a EU country and it is built upon already existing curricola and VET courses, integrating them whenever  a “competency gap” is identified. Each CLM is composed by a number of Learning Units (defined in term of knowledge, skills and competences – according to ECVET)
    </p>
