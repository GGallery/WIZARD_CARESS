    <h5>What are Learning Outcomes?</h5>

    <p>They are statements of what a learner knows, understands and is able to do at the end of a learning process; they are defined in terms of knowledge, skills and competences.</p>

    <h5>What’s the difference between Learning Outcomes and Competencies?</h5>

    <p>Competencies are actually achieved learning outcomes, validated by the ability of the learner to apply autonomously his/her knowledge and skills in practice, in society and at work.</p>

    <p>In order to define the Learning Outcomes of your CLM, you first have to analyze your “baseline course” with respect to the defined “competency gap”.</p>

