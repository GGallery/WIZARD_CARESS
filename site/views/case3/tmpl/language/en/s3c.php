    <p>
        The definition of assessment criteria is often put off to the last phases of the design process. Actually, after defining the learning outcomes and strategies of your CLM, you already have the main information necessary to define a consistent set of assessment criteria
    </p>

    <span class="focus">
        <p>DEFINE ASSESSMENT CRITERIA and CONDITIONS</p>
    </span>

    <p>
        <strong>We defined learning outcomes by using “active verbs”</strong> (Action 2 – Step 3), that helps us to identify <strong>what the learner should actually do to show he/she masters the competence</strong>. The Learning Outcome should also state the expected level of the competence  as well as <strong>the level of responsibility and autonomy of the learner</strong>.
    </p>

    <p>
        To define the assessment CRITERIA this information should be integrated with the <strong>definition of the ASSESSEMENT CONDITIONS</strong>
    </p>

    <p>
        Sometimes a <strong>description of the professional situation </strong>could be very useful, as well as the reference to the Key Activities analyzed at the beginning of the design process (Activity 1 – Step 2)
        <strong>Additional conditions (requirements)</strong>, can be also defined; for instance to emulate real-life professional working conditions, a limited amount of time can be assigned daily for each activity, so that the person under assessment operates in similar stress conditions; the equipment available must be clearly defined, as well as specific products and technical support in accordance to the professional situation under evaluation.
    </p>
    <p>
        Then the design of the assessment should go deeper <strong>defining specific tools </strong>(such as tests) or activities (such as problem-solving activities, demonstrations, etc.)
    </p>

    <span class="focus">
        <p>DEFINE ASSESSMENT TOOLS AND ACTIVITIES</p>
    </span>

    <p> The organization of complex situated learning activities, such as an apprenticeship or work-based learning, could also allow to assess the learner in a real context with respect to a number of skills
        and transversal competences. What is extremely important for assessment is that the designer should <strong>be aware of the competences</strong> (or their components) he/she should assess during the <strong>apprenticeship/learning
        on the job</strong> and organize a proper setting for assessment, according to the defined criteria.</p>
    <p> To this end, it could be important to integrate information about assessment in the design tools we’ve used so far (design tables).</p>

