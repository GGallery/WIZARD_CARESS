<span class="focus">
        <p>IDENTIFICA LA GIUSTA STRATEGIA DI APPRENDIMENTO PER CIASCUN RISULTATO DI APPRENDIMENTO</p>
    </span>

<p>
    Non esiste alcuna formula magica per la progettazione didattica e non c’è alcuna strategia di apprendimento efficace per ogni contesto, utente target e risultato di apprendimento. Ma, <strong>tenendo presenti i tuoi “vincoli progettuali” </strong>è possibile <strong>definire la strategia di apprendimento più adeguata per ciascun risultato di apprendimento</strong>.
</p>

<p>Potresti formalizzare la scelta delle tue strategie di apprendimento includendole nella tabella di progettazione usata precedentemente per dettagliare i risultati di apprendimento (Attività 2 – Fase 3)</p>

<a href="components/com_wizard/files/annex5_it.docx" class="templateLink" target="_blank">Clicca sull’icona per SCARICARE un template utile a definire una strategia per i tuoi Risultati di Apprendimento</a>

<p>
    Da un lato, ogni strategia di apprendimento (e la relativa attività di apprendimento) <strong>può perseguire più di un risultato di apprendimento; d’altro canto, si può mirare a ottenere ciascun risultato di apprendimento mediante l’utilizzo di strategie di apprendimento diverse</strong>. Pertanto, dopo aver identificato la giusta strategia di apprendimento per ciascun risultato di apprendimento, cerca di raggrupparli in base alla strategia.
</p>

<p>Così, se necessario</p>

<span class="focus">
        <p>Fai una REVISIONE DEL RAGGRUPPAMENTO di risultati di apprendimento in unita’ di apprendimento, tenendo presenti le strategie di apprendimento</p>
    </span>

<p>
Quanto all’identificazione delle giuste Strategie di Apprendimento, gli esperti hanno formalizzato e condiviso un numero di teorie, buone pratiche e modelli.
</p>


<a href="components/com_wizard/files/annex6_it.pdf" class="templateLink" target="_blank">Clicca sull’icona per SCARICARE qualche suggerimento da prendere in considerazione per definire la corretta strategia di apprendimento.</a>

<p>
    Se i “vincoli progettuali” ti permettono/impongono di prevedere <strong>uno o più moduli e-learning</strong>, è giunto il momento di definirli. Specialmente nella formazione degli adulti il tempo per l’apprendimento è particolarmente prezioso e quindi, quando ci sono condizioni contestuali favorevoli, è consigliabile utilizzare l’e-learning per raggiungere specifici obiettivi di apprendimento.
</p>


<span class="focus">
        <p>IDENTIFICA I POSSIBILI MODULI E-LEARNING </p>
    </span>

<p>
    Materiali interattivi multimediali, video o documenti scaricati possono spesso sostituire una tradizionale lezione in presenza, quando essa ha come obiettivo conoscenze teoriche o pratiche.
</p>

<p>
    Inoltre, l’e-learning può essere usato anche per raggiungere altri tipi di obiettivi di apprendimento e per mettere in pratica strategie di apprendimento più attive.
</p>

