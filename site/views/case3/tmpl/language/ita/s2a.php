<p>
Una delle prime fasi nella progettazione di un processo di apprendimento è la definizione dei <strong>vincoli contestuali</strong>. 
Il tuo obiettivo è progettare un MAC, basato possibilmente su corsi già esistenti piuttosto che creare un intero nuovo corso e cercare di migliorarlo; pertanto è importante innanzitutto: 

</p>

<span class="focus">
        <p>IDENTIFICARE IL PROPRIO TARGET SPECIFICO (gli studenti)</p>
    </span>

<p>
    Anche se in questa fase il professionista oggetto della tua ricerca e il suo paese di provenienza dovrebbero già essere stati definiti, <strong>gli utenti di riferimento devono ancora essere definiti nel dettaglio</strong>.
</p>

<span class="exampleBox">
Per esempio, se scegli “INFERMIERI IN ITALIA” dovresti essere più specifico: 
    <em>“quale tipo di infermiere? laureato negli ultimi 5 anni? con una specializzazione particolare?“</em>
Dal 1999 per diventare infermiere è necessario conseguire una laurea triennale, ma molti infermieri che lavorano nell’assistenza domiciliare non sono laureati. 
    <strong>Quali sono i nostri utenti-target specifici?</strong>

</span>

<p>
    Dopo aver definito il target specifico, puoi
</p>
<span class="focus">
        <p>IDENTIFICARE IL CORSO ESISTENTE/CURRICULUM che vuoi migliorare/integrare</p>
    </span>

    <p>
     In particolare, puoi:
</p>

<ul class="listapallino">
    <li>scegliere un corso di formazione/aggiornamento sull’assistenza domiciliare già esistente (preferibile se disponibile)</li>
    <li>scegliere un corso di formazione/aggiornamento già esistente, non specificatamente incentrato sull’assistenza domiciliare ma che miri all’acquisizione di alcune abilità che desideri affrontare;</li>
    <li>scegliere un corso preparatorio generale, come una laurea in scienze infermieristiche, sulla base del quale puoi creare un corso di specializzazione.</li>

    </li>
</ul>

Da adesso in poi chiameremo questo corso il tuo <strong>“corso di base”</strong>
