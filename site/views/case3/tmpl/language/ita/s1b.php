<h5>Cos’è una competenza?</h5>

<p>Nel progetto CARESS la parola competenza è convenzionalmente usata per descrivere la capacità di mettere in pratica o utilizzare una serie di conoscenze e abilità correlate (con un certo livello di responsabilità e autonomia) necessarie in un determinato contesto lavorativo allo scopo di svolgere incarichi o “funzioni di lavoro fondamentali” in modo efficace; questa caratteristica “include evidenti competenze fatte di conoscenze e abilità, ma anche elementi sottesi a tali competenze, come tratti distintivi e motivazioni”.</p>

<span class="focus">
