<h5>Cosa sono le Attività Chiave?</h5>
<p>
    <strong>Le attività chiave sono un gruppo integrato di competenze</strong>, che sono necessarie nella loro interezza per svolgere un compito pertinente a un profilo professionale. Le attività chiave di una professione devono comprendere tutte le attività necessarie per lo svolgimento di quella professione, indipendentemente dal suo contesto applicativo.
Esempi di attività chiave potrebbero essere “educare gli utenti e i loro caregiver” oppure “monitorare la fragilità e le condizioni di salute”. Altre attività chiave potrebbero essere anche raggruppate in “RUOLI” professionali (consulta il Glossario CARESS), come “Prevenzione ed educazione sanitaria” o “Ricerca”.
    <br>
    <em>Esempi di attività chiave potrebbero essere “educare gli utenti e i loro badanti” ovvero “monitorare la fragilità e le condizioni di salute”. Altre attività chiave potrebbero essere anche raggruppate in “RUOLI” professionali (confronta il Glossario CARESS), come “Prevenzione ed educazione sanitaria” o “Ricerca”. </em>
</p>

<span class="focus">
        <p>DEFINISCI LE COMPETENZE che dovrebbero caratterizzare ogni “attività chiave” ed ELENCALE</p>
    </span>

<p>Le competenze possono essere definite come le “conoscenze” e “abilità” necessarie per svolgere le suddette attività chiave con il livello di responsabilità e autonomia che è richiesto al professionista per svolgere l’attività chiave.
Inoltre, puoi fare una distinzione tra:
</p>

<ul class="listapallino">
    <li>
        <em>competenze che sono orizzontali rispetto ai diversi professionisti coinvolti nell’assistenza domiciliare </em> (divario di competenza trasversale), come la conoscenza di regole e leggi riguardanti l’assistenza domiciliare o le abilità pratiche sull’uso delle ICT per il monitoraggio della salute, ecc. 
    <li>
        <em>competenze che sono specifiche del professionista interessato </em>, come abilità pratiche riguardanti gli esercizi terapeutici rivolti agli anziani per i fisioterapisti, o la conoscenza della rete locale dei servizi sociali per gli assistenti domiciliari. 
    </li>
</ul>

