<h5>Cosa sono le conoscenze, le abilità e attitudini personali e trasversali?</h5>

<p>
    <strong>Le CONOSCENZE </strong>sono il risultato dell’assimilazione delle informazioni attraverso l’apprendimento. Le conoscenze sono l’insieme dei fatti, principi, teorie e pratiche relativo al settore di lavoro o di studio.
</p>
<p>
    <strong>Le ABILITÀ  </strong>sono le capacità di applicare le conoscenze e usare le competenze tecniche per portare a termine dei compiti e risolvere dei problemi.
</p>

<p><strong>LE ATTITUDINI PERSONALI </strong>includono capacità personali, sociali e/o metodologiche che possono essere utilizzate nella società o al lavoro.</p>

<p><strong>LE ATTITUDINI TRASVERSALI </strong>sono quelle tipicamente considerate non direttamente collegate a un particolare lavoro, compito, disciplina accademica o area di conoscenza, ma competenze che possono essere usate in un’ampia varietà di situazioni e ambienti lavorativi. </p>
<p>Nella definizione di un “risultato di apprendimento” le <strong>ATTITUDINI PERSONALI E TRASVERSALI </strong>sono descritte in termini di “attitudini necessarie per applicare una conoscenza e/o un’abilità nel <strong>contesto lavorativo con un certo livello di responsabilità e autonomia”</strong>.</p>

