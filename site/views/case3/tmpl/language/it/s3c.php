    <p>
        La definizione dei criteri di valutazione è spesso rimandata alle fasi finali del processo di progettazione. In effetti, però, dopo aver definito i risultati di apprendimento e le strategie del tuo MAC, sei già in possesso delle principali informazioni necessarie per definire un insieme coerente di criteri di valutazione.
    </p>

    <span class="focus">
        <p>DEFINISCI I CRITERI e LE CONDIZIONI di VALUTAZIONE</p>
    </span>

    <p>
        <strong>Abbiamo definito i risultati di apprendimento utilizzando “verbi attivi” </strong> (Azione 2 – Fase 3), che aiutano a identificare <strong>ciò che lo studente dovrebbe effettivamente fare per dimostrare come padroneggia la competenza</strong>. I Risultati di apprendimento dovrebbero anche definire <strong>il livello di competenza atteso e il livello di responsabilità e autonomia dello studente</strong>.
    </p>

    <p>
        Allo scopo di definire i CRITERI di valutazione, queste informazioni andrebbero integrate con la <strong>definizione delle CONDIZIONI DI VALUTAZIONE</strong>
    </p>

    <p>
        Talvolta potrebbe rivelarsi utile una <strong>descrizione della situazione professionale </strong>insieme al riferimento alle Attività Chiave analizzate all’inizio del processo di progettazione (Attività 1 – Fase 2). È anche possibile definire
        <strong>Condizioni (requisiti) aggiuntive</strong>; per esempio, per emulare condizioni lavorative professionali mutuate dalla vita reale, si può assegnare un periodo di tempo limitato a ciascuna attività, in modo tale che la persona soggetta a valutazione operi in condizioni di stress simili a quelle della vita lavorativa reale; devono essere chiaramente definiti l’attrezzatura necessaria e i prodotti specifici e i supporti tecnici coerenti con la situazione professionale soggetta a valutazione.
    </p>
    <p>
        Successivamente, la progettazione della valutazione dovrebbe diventare più capillare <strong>definendo strumenti specifici </strong>(come test) <strong>o attività </strong> (come attività di problem-solving, dimostrazioni, ecc.).
    </p>

    <span class="focus">
        <p>DEFINISCI GLI STRUMENTI e LE ATTIVITA’ DI VALUTAZIONE</p>
    </span>

    <p> L’organizzazione di attività complesse di apprendimento situato, come tirocini o apprendimento sul lavoro, potrebbe altresì permettere di valutare lo studente in un contesto reale rispetto a abilità e competenze trasversali. Ciò che si dimostra estremamente importante per la valutazione è il fatto che chi progetta dovrebbe <strong>essere consapevole delle competenze </strong> (o delle componenti) che vanno valutate durante il <strong>tirocinio/apprendimento sul lavoro </strong>e dovrebbe organizzare una adeguata impostazione della valutazione, in base ai criteri definiti.</p>
   
    <p>A tale scopo, sarebbe importante integrare le informazioni sulla valutazione negli strumenti di progettazione finora utilizzati (tavole di progettazione).</p>

