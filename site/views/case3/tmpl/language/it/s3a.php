<p>
    Dopo aver identificato un elenco di risultati di apprendimento e averli raggruppati in Unità di Apprendimento, la fase successiva sarà definire la specifica strategia di apprendimento che utilizzerai per ciascun risultato.
</p>
<p>
    In questa fase dovresti prendere in considerazione tutti i <strong>“vincoli del progetto” che caratterizzano sia il corso di base sia il MAC</strong>.
</p>
Hai già analizzato alcuni vincoli contestuali (Azione 1 – Fase 1), ma devi accertarti di prendere in considerazione una serie di aspetti.
</p>


<span class="focus">
        <p>RISPONDI ALLE SEGUENTI DOMANDE</p>
    </span>

<ul class="listaspunta">
    <li>Quali sono le caratteristiche principali degli utenti a cui ti rivolgi (gli studenti)? Sono principalmente lavoratori? Sono neolaureati? Quanto tempo possono dedicare quotidianamente alla formazione continua? Sarebbe utile per loro avere esperienze lavorative pratiche sul campo? Sarebbe utile per loro frequentare parte del corso in modalità e-learning? Posseggono adeguate competenze ICT?</li>
    <li>C’è qualche legge o norma (nazionale o locale) per la formazione continua in assistenza domiciliare che potrebbe avere ricadute sull’organizzazione del tuo corso? Pensa al numero di ore fissate per il corso, i prerequisiti per il riconoscimento della qualifica, ecc. L’apprendimento informale può essere riconosciuto in qualche modo?</li>
    <li>Quali sono le risorse (economiche, logistiche, tecniche, risorse umane, ecc.) su cui puoi fare affidamento? Puoi investire denaro? Sei in possesso di materiale di apprendimento e altre risorse che puoi riutilizzare in questo corso? Puoi produrre nuovo materiale? Puoi pagare degli esperti? Hai le risorse per garantire il coinvolgimento di insegnanti adeguati? Hai locali e attrezzature adeguati all’insegnamento in presenza? Possiedi una piattaforma e-learning?</li>
</ul>

<p>Generalmente, dovresti tenere in considerazione che:</p>

<ul>
    <li>La pratica è essenziale per l’assistenza domiciliare agli anziani e le politiche UE incoraggiano sempre di più l’apprendimento basato sull’esperienza; ma “l’apprendimento basato sull’esperienza sul campo” potrebbe essere difficilmente gestibile nel momento in cui il “luogo di lavoro” è la casa dell’anziano; in alcuni paesi i tirocini sono spesso limitati a contesti istituzionali o a residenze per anziani, a causa di problemi organizzativi e assicurativi, mentre in altri paesi (specialmente nel Nord Europa) il tirocinio è ben integrato nel sistema di formazione continua. </li>
    <li>Le conoscenze dovrebbero essere <strong>inquadrate, contestualizzate e collegate alla pratica</strong>e ciò è particolarmente vero nel campo dell’Assistenza Sociosanitaria: schemi mentali, atteggiamenti e procedure sono fondamentali quanto le competenze trasversali, sociali e personali. Per sviluppare queste competenze, si raccomandano vivamente processi di apprendimento non formali e informali.</li>
    <li>Ogni paese o regione ha solitamente le sue <strong>regole specifiche riguardanti il percorso di formazione continua</strong>; esse vanno prese in considerazione per organizzare un corso che potrebbe essere riconosciuto da enti regolatori.</li>
</ul>

