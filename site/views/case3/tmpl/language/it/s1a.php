<p>
    Per definire un MAC per un determinato professionista in un paese UE, il primo passo da fare è identificare lo specifico divario di competenze da colmare. Bisogna prendere in considerazione una serie di aspetti e fare riferimento a informazioni e dati riguardanti il professionista e il paese presi in considerazione.
</p>

<span class="focus">
        <p>RACCOGLI LE SEGUENTI INFORMAZIONI</p>
    </span>

<ul class="listapallino">
    <li><strong>Competenze effettive del professionista e mansioni svolte:</strong></li>
    <ul class="listaspunta">
        <li>Quali sono le competenze effettive dei professionisti che operano nell’ambito dell’assistenza domiciliare nel paese selezionato? 
        </li>
        <li>Quali compiti svolge effettivamente il professionista a casa dell’anziano? Quali necessità dell’anziano soddisfa? 
        </li>

    </ul>


    <li><strong>Livello d’istruzione e percorso formativo del professionista:</strong></li>
    <ul class="listaspunta">
        <li>Che formazione deve avere il professionista per lavorare nel settore dell’assistenza domiciliare?
        </li>
        <li>Ci sono corsi di formazione specifici per l’assistenza domiciliare agli anziani rivolti a questa figura professionale?
        </li>

    </ul>


    <li><strong>Esigenze relative al contesto: </strong></li>
    <ul class="listaspunta">
        <li>Quali sono le esigenze principali dell’assistenza domiciliare nel paese selezionato?
        </li>
        <li>Com’è organizzata l’assistenza domiciliare in quel paese? Quali sono le necessità organizzative che ne derivano?
        </li>
        <li>Il professionista necessita di lavorare in gruppo o in stretta collaborazione con altri professionisti? 
        </li>
        <li>Si utilizzano tecnologie ICT per sostenere l’assistenza domiciliare? Quali competenze sono necessarie per usarle? 
        </li>
    </ul>

</ul>

<h5>Per raccogliere queste informazioni puoi cercare nel Sistema CARESS <a href="http://framework.project-caress.eu" target="_blank">http://framework.project-caress.eu</a> </h5>

<p>Se qualche informazione non dovesse essere disponibile per un determinato paese, puoi provare a cercarla in un paese “prossimo” o affine. É anche possibile fare riferimento ai 9 principali tipi di professionisti dell’assistenza domiciliare definiti nel sistema e raccogliere informazioni su professioni simili. Dovresti integrare le informazioni raccolte con le tue esperienze personali per contestualizzarle meglio.</p>

