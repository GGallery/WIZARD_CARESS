<h5>Cos’è un Modulo di Apprendimento Compensativo?</h5>

<p>
É un modulo che mira a colmare uno specifico “divario di competenze” ed è rivolto a uno specifico professionista dell’assistenza domiciliare in uno dei paesi dell’Unione Europea; il modulo viene definito “compensativo” poiché si basa su curricula e corsi di formazione già esistenti, integrandoli quando emerge un divario di competenza. Ogni MAC è costituito da un numero di Unità di Apprendimento (definite in termini di conoscenze, abilità e competenze – in conformità alle raccomandazioni ECVET). Consulta il glossario per approfondimenti.
</p>
