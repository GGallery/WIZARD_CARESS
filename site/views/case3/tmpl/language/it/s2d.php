    <h5>Cosa sono le Unità di Apprendimento (Learning Units)?</h5>

    <p>Sono componenti di un percorso di qualifica e consistono in un insieme coerente di conoscenze, abilità e attitudini che possono essere valutate e convalidate. </p>
    <p>Per creare Unità di Apprendimento è necessario definire un insieme coerente di Risultati di Apprendimento (Learning Outcomes). Questi risultati possono essere parte di uno specifico corso di specializzazione o possono riferirsi all’adempimento di un definito compito professionale.</p>

