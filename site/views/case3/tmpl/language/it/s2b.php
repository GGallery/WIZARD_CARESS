<h5>Cosa sono i Risultati di Apprendimento (Learning Outcomes)?</h5>

<p>Secondo lo standard europeo ECVET, sono la descrizione di ciò che uno studente sa, capisce ed è in grado di fare alla fine del processo di apprendimento; sono definiti in termini di conoscenza (knowledge), abilità (skills) e attitudini personali e trasversali (personal and transversal competence). </p>

<h5>Qual è la differenza tra Risultati di Apprendimento e Competenze?</h5>

<p>Le competenze (competency) sono risultati di apprendimento effettivamente raggiunti, convalidati dall’abilità dello studente di applicare autonomamente le sue conoscenze e abilità nella pratica, nella società e al lavoro.</p>

<p>Per definire i risultati di apprendimento del tuo MAC, devi prima analizzare il tuo “corso di base” confrontandolo con il “divario di competenza” definito: si sovrappongono? In cosa differiscono?</p>

